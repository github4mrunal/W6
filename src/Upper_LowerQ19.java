import java.util.Locale;
import java.util.Scanner;

//19. Write a Java program to convert a given string into lowercase.
// Sample Input: THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.
// Output: the quick brown fox jumps over the lazy dog.
public class Upper_LowerQ19 {//class
    public static void main(String[] args) {//main method
        String name  =("THE QUICK BROWN FOX JUMPS OVER THE LAZY DOG.");//datatype and variale
        name.toLowerCase(Locale.ROOT);//method
        System.out.println(name.toLowerCase(Locale.ROOT));//print statement


    }



}
