//15. Write a Java program to swap two variables.

public class Swap_2Q15 {//class
    public static void main(String[] args) {//main method


        String a = "Prime Testing";//datatype variable
        String b = "Testing Prime";//datatype variable

        String swp;//datatype defined temp variable
        swp = a;//variable
        a = b;//method
        b = swp;//method
        System.out.println(a);//print statement
        System.out.println(b);//print statement

    }
}
